import turtle
kalem=turtle.Turtle()
kalem.pencolor("red") # kalemin rengini kırmızı yapar.
kalem.pensize(3) # kalemin kalınlığını 3 e alır.
kalem.forward(100)# 100 birim ileri gider.
kalem.left(90)# 90 derece sola döner.
kalem.color("#ACE515") #kalemin rengini #ACE515 yapar.
kalem.pensize(8) # kalemin kalınlığını 8 e alır.
kalem.forward(100)# 100 birim ileri gider.
kalem.left(90)# 90 derece sola döner.
kalem.pencolor("yellow")# kalemin rengini sarı yapar.
kalem.pensize(6)# kalemin kalınlığını 6 ya alır.
kalem.forward(100)# 100 birim ileri gider.
kalem.left(90)# 90 derece sola döner.
kalem.color("#2259C1")#kalemin rengini #2259C1 yapar.
kalem.pensize(1)# kalemin kalınlığını 1 e alır.
kalem.forward(100)# 100 birim ileri gider.
kalem.left (90)# 90 derece sola döner.
turtle.done()#çizimin ekranda kalmasını sağlar.
