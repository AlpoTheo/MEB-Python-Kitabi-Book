import turtle
kalem=turtle.Turtle()
kalem.pencolor("orange")#kalemin rengi turuncu olur.
kalem.pensize(2)#kalemin kalınlığı 2 ye alınır.
kalem.circle(20)# 20 derece bir daire çizilir.
kalem.pencolor("red")#kalemin rengi kırmızı olur.
kalem.circle(30)# 30 derece bir daire çizilir.
kalem.pencolor("blue")#kalemin rengi mavi olur.
kalem.circle(40)# 40 derece bir daire çizilir.
turtle.done()