print(5==3) #“==” operatörü iki değerin birbirine eşit olup olmadığını anlamak için kullanılır. 5, 3 e eşit ise True değilse False olur ve ekrana yazdırılır.
sayi1=5
sayi2=3
print(sayi1==sayi2) # 5, 3 eşit olmadığı için False çıkar ve ekrana yazdırılır.
print(sayi1==5) # sayi1 in değeri 5 olduğu için 5 ile eşitliği sorgulanınca True çıkar ve ekrana yazdırılır.