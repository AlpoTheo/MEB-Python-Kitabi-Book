#“!=” operatörü karakter dizilerinde de değerlerin farklılığını kontrol etmek için kullanılır.

print('Emre'!='emre') #Emre değeri ile emre değeri birbirine eşit olmadıkları için True değeri çıkar ve ekrana yazdırılır.
metin1='Emre'
metin2='emre'
print(metin1!=metin2) # metin1 ve metin2 nin değerleri Büyük küçük harf duyarlılığından ötürü eşit olmaz bu yüzden True değeri çıkar ve ekrana yazdırılır.
print(metin1!='Emre') #metin1 in değeri ile Emre değeri eşit oldukları için False değeri çıkar ve ekrana yazdırılır.