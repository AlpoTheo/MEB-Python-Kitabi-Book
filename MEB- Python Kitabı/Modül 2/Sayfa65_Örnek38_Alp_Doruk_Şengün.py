#“!=” operatörü iki değerin birbirinden farklı olup olmadığını anlamak için kullanılır. “==” operatörünün tersine değerler birbirine eşitse “False”, eşit değilse “True” değerini döndürür.
print(5!=3) # Değerler birbirine eşit olmadığı için True değeri çıkar ve ekrana yazdırılır.
sayi1=5
sayi2=3
print(sayi1!=sayi2) #sayi1 ve sayi2 nin değerleri birbirine eşit olmadıkları için True değeri çıkar ve ekrana yazdırılır.
print(sayi1!=5) #sayi1 in değeri ile 5 sayısı eşit oldukları için False değeri çıkar ve ekrana yazdırılır.
# Çıktıların == operatörünün tersi olduğuna dikkat ediniz.