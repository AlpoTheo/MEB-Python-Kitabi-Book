liste=list(range(1,15,2)) # 1 den 15 e kadar 2 nin katları olmayacak şekilde sayılar alınan bir liste oluşturulur.
print(liste)
liste.sort() # liste küçükten büyüğe doğru sıralanır.
print(liste)
liste.reverse() # liste nin sıralaması ters çevrilir. Büyükten küçüğe doğru sıralanmış olur.
print(liste)